var color = prompt("Traffic Lights Color");

//var color= "red";

if (color == "red") {
               document.write("<b>Must Stop</b>");
            } 
else if (color == "yellow") {
               document.write("<b>Ready to Move</b>");
            } 
else if (color == "green") {
               document.write("<b>Move Now</b>");
            }
